public class Horse
{
    private String name;
    private String color;
    private int birthYear;

  public String getName()
    {
       return name;
    }
  public void setName(String name)
    {
       this.name = name;
    }
  
  public String getColor()
    {
       return color;
    }
  public void setColor(String color)
    {
      this.color = color;
    }

  public int getYear()
    {
       return birthYear;
    }
  public void setYear(int year)
    {
       birthYear = year;
    }
}